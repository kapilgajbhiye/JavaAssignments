//Write a Java program to display the default value of all primitive data types of Java ?
package MyAssignment_1;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		System.out.println("There are 8 types of primitive data types in java:");
		System.out.println(" ");
		
		System.out.println("DATA TYPES"+"    DEFAULT VALUES");
		System.out.println("1.boolean" + "   Default value = 1 bit(True/false)");
		System.out.println("2.byte   " + "   Default value = 1 byte");
		System.out.println("3.short  " + "   Default value = 2 byte");
		System.out.println("4.char   " + "   Default value = 2 byte");
		System.out.println("5.int    " + "   Default value = 4 byte");
		System.out.println("6.float  " + "   Default value = 4 byte");
		System.out.println("7.long   " + "   Default value = 8 byte");
		System.out.println("8.double " + "   Default value = 8 byte");
	}

}
